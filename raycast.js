class RayCast {
    constructor(startX, startY, endX, endY, length, i, j, viewHorizontal) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
        this.length = length;
        this.i = i;
        this.j = j;
        this.viewHorizontal = viewHorizontal;
    }
}